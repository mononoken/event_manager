def clean_zipcode(zipcode)
  zipcode.to_s.rjust(5, '0')[0..4]
end

def clean_phone_number(phone_num)
  clean_phone_num = phone_num.to_s.split('').filter { |num| num == '0' || num.to_i != 0 }.join
  if clean_phone_num.length == 10
    clean_phone_num
  elsif clean_phone_num.length == 11 && clean_phone_num[0] == '1'
    clean_phone_num[1, 10]
  else
    '0000000000'
  end
end

def legislators_by_zipcode(zip)
